#DROP TABLE IF EXISTS staff;
#创建员工的个人信息表
CREATE TABLE staff(
	NAME VARCHAR(255) COLLATE UTF8_BIN NOT NULL,
	SEX VARCHAR(255) COLLATE UTF8_BIN NOT NULL,
	STA_NO VARCHAR(255) COLLATE UTF8_BIN NOT NULL,
	BIRTH DATETIME,
	DEPT VARCHAR(255) COLLATE UTF8_BIN NOT NULL,
	JOB VARCHAR(255) COLLATE UTF8_BIN NOT NULL,
	EDU VARCHAR(255) COLLATE UTF8_BIN NOT NULL,
	ADDRESS VARCHAR(255) COLLATE UTF8_BIN NOT NULL,
	TEL VARCHAR(255) COLLATE UTF8_BIN NOT NULL,
	introduction VARCHAR(255) COLLATE UTF8_BIN NOT NULL,
	PASSWORD VARCHAR(255) COLLATE UTF8_BIN NOT NULL
)ENGINE=INNODB DEFAULT CHARSET=UTF8;

