#创建员工培训安排表
CREATE TABLE train(
	NAME VARCHAR(255) COLLATE UTF8_BIN,
	STA_ID VARCHAR(255) COLLATE UTF8_BIN,
	COURSE VARCHAR(255) COLLATE UTF8_BIN,
	SCORE VARCHAR(255) COLLATE UTF8_BIN,
	EVALUATE  VARCHAR(255) COLLATE UTF8_BIN,
	CHECK_TIME DATETIME
)ENGINE=INNODB DEFAULT CHARSET=UTF8;
 

#DROP TABLE IF EXISTS course;
#创建课程表
CREATE TABLE course(
	NO VARCHAR(255) COLLATE UTF8_BIN  NOT NULL,#课程号
	NAME VARCHAR(255) COLLATE UTF8_BIN NOT NULL,#课程
	TEACHER VARCHAR(255) COLLATE UTF8_BIN NOT NULL,#老师
	INTRO VARCHAR(255) COLLATE UTF8_BIN NOT NULL,
	BOOK VARCHAR(255) COLLATE UTF8_BIN NOT NULL,
	START_TIME DATE,
	END_TIME DATE,
	CLASS_TIME VARCHAR(255) COLLATE UTF8_BIN,
    MAX_STAFFS INT(10),
	CLASS_ADDRESS VARCHAR(255) COLLATE UTF8_BIN,
    STATE VARCHAR(255) COLLATE UTF8_BIN NOT NULL
)ENGINE=INNODB DEFAULT CHARSET=UTF8;
 
